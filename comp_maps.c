#include "fillit.h"

static int			ft_comp_map(const char *s1, const char *s2)
{
	int	i;
	int	c;
	int	n;

	i = 0;
	c = 0;
	n = 0;
	while (n != 20)
	{
		if (s1[i] != s2[c])
			return (0);
		i++;
		c++;
		n++;
	}
	return (1);
}

